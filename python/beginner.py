# Mathematical operations for beginners
# Addition = +
# Subtraction = -
# Multiplication = *
# Division = /
# Exponentiation = **
# Modulus = %
# Floor Division = //
# Example usage of each operation

#PEMDAS
# Parentheses
result1 = (2 + 3) * 4  # Parentheses first
print("Result of (2 + 3) * 4 =", result1)

# Exponents
result2 = 2 ** 3 + 5  # Exponents next
print("Result of 2 ** 3 + 5 =", result2)

# Multiplication and Division
result3 = 10 / 2 * 3  # Left to right
print("Result of 10 / 2 * 3 =", result3)

# Addition and Subtraction
result4 = 15 - 5 + 2  # Left to right
print("Result of 15 - 5 + 2 =", result4)

# Demonstrating each operation
a = 10
b = 3

addition = a + b
subtraction = a - b
multiplication = a * b
division = a / b
exponentiation = a ** b
modulus = a % b
floor_division = a // b

print("Addition:", addition)
print("Subtraction:", subtraction)
print("Multiplication:", multiplication)
print("Division:", division)
print("Exponentiation:", exponentiation)
print("Modulus:", modulus)
print("Floor Division:", floor_division)

