# Comparison operators in Python
a = 10
b = 20
# Equal to
print("a == b:", a == b)  # False

# Not equal to
print("a != b:", a != b)  # True

# Greater than
print("a > b:", a > b)    # False

# Less than
print("a < b:", a < b)    # True

# Greater than or equal to
print("a >= b:", a >= b)  # False

# Less than or equal to
print("a <= b:", a <= b)  # True

# Chained comparison
print("5 < a < 15:", 5 < a < 15)  # True
# Output:
# a == b: False
# a != b: True
# a > b: False
# a < b: True
# a >= b: False
# a <= b: True
# 5 < a < 15: True
